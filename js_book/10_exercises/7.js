/* What is exception handling and what problem does it solve?

Exception handling ais a process that lets the program deal with errors
in a way that doesn't cause execution to be terminated. When our program 
gets into situations it can't control, we want it to be able to perhaps output
some kind of error message and then move away from the circumstance, and
exception handling lets us take care of that. It is implemented with `try/catch`
statements. This lets us prevent something like a catastrophic failure or
bug that might cause issues down the line.
*/