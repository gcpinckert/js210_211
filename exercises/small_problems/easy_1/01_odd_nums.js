/*
input: none
output: all odd numbers from 1-99 inclusive
  - each number should be logged on the console on its own line

Algorithm
- Iterate from 1 up to 99 (inclusive)
 - log the current iteration number to the console
 - increment the iteration number by 2
*/

for (let iteration = 1; iteration < 100; iteration += 2) {
  console.log(iteration);
}

